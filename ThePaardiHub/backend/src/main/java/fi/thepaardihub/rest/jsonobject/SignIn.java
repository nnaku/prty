package fi.thepaardihub.rest.jsonobject;

public class SignIn {
	String username;
	String psw;
	String pswVerify;
	String firstname;
	String lastname;
	String email;
	
	public String getUsername() {
		return username;
	}
	public String getPsw() {
		return psw;
	}
	public String getPswVerify() {
		return pswVerify;
	}
	public String getFirstname() {
		return firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public String getEmail() {
		return email;
	}
	

}
 